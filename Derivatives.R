# This functions are the derivatives of the Self decomposable model in order to 
# calculate the likelihood of the observations
# To find the probability mass function we can use the pgf using the rule that 
# phi^(x)(0)/x!=f(x), thus we need to compute the x derivative of phi(g)

phi.ge.0	<-	function(z,a,b,gam){
	
	((b+a*(1-z))/(b+1-z))^gam
	
}

phi.ge.1	<- function(z,a,b,gam){
	
	gam*((b+a*(1-z))/(b+1-z))^(gam-1)*((b*(1-a))/(b+1)^2)
	
}

phi.g.0 <- function(z,a,b,x,gam)
{
	
	((1-a + a*z)^x)*((b+a*(1-z))/(b+1-z))^gam
}
